
public class ArthimaticOperator {

	public static void main(String[] args) {
		int num1 = 1;
		
		num1++;
		num1 = num1 + 1;
		
		num1--;
		num1 = num1 - 1;
		
		num1+=5;
		num1 = num1 + 5;
		
		num1-=2;
		num1 = num1 - 5;
		
		num1*=4;
		System.out.println(num1);
		
				

	}

}